def soma(a, b): #Defino minha função com o nome 'soma' com as variáveis 'a' e 'b'
    print(f'A = {a} e B = {b}') 
    s = a + b #Crio minha variável "s" dizendo o que ela vai fazer
    print(f'A soma de A + B é {s}')

